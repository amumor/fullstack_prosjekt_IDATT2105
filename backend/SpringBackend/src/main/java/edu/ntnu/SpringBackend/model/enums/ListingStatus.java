package edu.ntnu.SpringBackend.model.enums;

/**
 * Enum representing the status of a listing.
 * <p>
 * The status can be one of the following:
 * <ul>
 *   <li>ACTIVE: The listing is currently active and available for bidding.</li>
 *   <li>INACTIVE: The listing is not currently active.</li>
 *   <li>SOLD: The listing has been sold.</li>
 * </ul>
 * </p>
 */
public enum ListingStatus {
  ACTIVE,
  INACTIVE,
  SOLD,
}
